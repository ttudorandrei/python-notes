# Python is a dynamically typed language

a = 1  # int (Integer)
b = 2  # Int
c = 3.5  # Float (floating-point number)
d = "Hello"  # String

result = str((a + c) * b)
print(d)   # calling the "print" function

print(result, type(result))  # printing multiple values on the same line by separating them by a comma ","

